package loops;

public class basicloop {
    public static void main(String[] args) {
        for (int i = 1; i <=4 ; i++) {
            System.out.println("Shivani");
        }
        for (int i = 1; i <=10; i++) {
            System.out.print(i+" ");
        }
        System.out.println();
        for (int i = -8; i <= 8; i++) {
            System.out.print(i+" ");
        }
    }
}
